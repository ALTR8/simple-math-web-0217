def addition(num1, num2)
  sum = num1+num2
end

def subtraction(num1, num2)
  total = num1-num2
end

def division(num1, num2)
  quotient = num1/num2
end

def multiplication(num1, num2)
  multiple = num1*num2
end

def modulo(num1, num2)
  divisor = num1 % num2
end

def square_root(num)
  root = Math.sqrt(num)
end

def order_of_operation(num1, num2, num3, num4)
  order = num1 + (( num2 * num3 ) / num4 )
end
